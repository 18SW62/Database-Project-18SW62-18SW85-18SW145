    public ArrayList<Photo> phonelist() {
        Statement st;
        ResultSet rs;
        String selectAllSqlQuery = "SELECT * FROM `student`";
        ArrayList<Photo> phonelists = new ArrayList<Photo>();

        try {

            st = conn1.createStatement();

            rs = st.executeQuery(selectAllSqlQuery);

            while (rs.next()) {
                Photo p1 = new Photo();
                p1.setimagepath(rs.getString("ImagePath"));
                phonelists.add(p1);

            }

            return phonelists;

        } catch (SQLException ex) {
            Logger.getLogger(Student_Record.class.getName()).log(Level.SEVERE, null, ex);
        }
        return phonelists;
    }
