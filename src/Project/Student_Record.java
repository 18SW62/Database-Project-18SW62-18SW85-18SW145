    private void table_update(){
        int c;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn1=DriverManager.getConnection("jdbc:mysql://localhost/project","root","");
            insert=conn1.prepareStatement("insert into student(Student_Name,Student_FName,Student_Surname,Student_DOB,Student_Class,Student_Email)values(?,?,?,?,?,?)");
            insert=conn1.prepareStatement("Select * from student");
            ResultSet rs=insert.executeQuery();
            ResultSetMetaData Rss=rs.getMetaData();
            c=Rss.getColumnCount();
            DefaultTableModel df=(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2=new Vector();
                for(int a = 1;a<=c;a++){
                    v2.add(rs.getString("Student_id"));
                    v2.add(rs.getString("Student_Name"));
                    v2.add(rs.getString("Student_FName"));
                    v2.add(rs.getString("Student_Surname"));
                    v2.add(rs.getString("Student_DOB"));
                    v2.add(rs.getString("Student_Class"));
                    v2.add(rs.getString("Student_Email"));
                    v2.add(rs.getString("ImagePath"));
            }
                df.addRow(v2);
            }
            
            TxtName.setText("");
            TxtFname.setText("");
            TxtSurname.setText("");
            TxtDOB.setText("");
            TxtClass.setText("");
            TxtEmail.setText("");
            TxtName.requestFocus();
        } catch (ClassNotFoundException | SQLException ex) {
           JOptionPane.showMessageDialog(null,ex);
        }
    }
